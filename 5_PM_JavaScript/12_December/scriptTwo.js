//Difference between == operator and === operator

a = 4;
b = 6;

console.log("type of variable a is ", typeof (a));
console.log("type of variable b is ", typeof (b));

console.log("The output of == operator is ", a == b);
console.log("The output of === operator is ", a === b);
console.log("The output of != operator is ", a != b);